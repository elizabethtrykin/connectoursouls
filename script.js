let currentCategory = '';
let currentQuestionIndex = 0;

function selectCategory(category) {
    currentCategory = category;
    currentQuestionIndex = 0;
    document.getElementById('category-container').style.display = 'none';
    document.getElementById('question-container').style.display = 'flex';
    showNextQuestion();
}

function showNextQuestion() {
    const questions = categories[currentCategory];
    const randomIndex = Math.floor(Math.random() * questions.length); // Generate a random index
    const questionNumber = randomIndex + 1; // Adjust index to be human-readable (starting from 1)
    const questionText = questionNumber + ". " + questions[randomIndex]; // Get random question with number
    document.getElementById('question').innerText = questionText; // Display the question
}


function goBack(event) {
    event.stopPropagation(); // Prevent the question change when clicking 'Back'
    document.getElementById('question-container').style.display = 'none';
    document.getElementById('category-container').style.display = 'flex';
}

window.onload = function() {
    document.getElementById('category-container').style.display = 'flex';
};
